package com.coke.tasks;

public class HierarchyPOJO {
	private String Year;
    private String Quarter;
    private String Month;
    private String Date;
    
    public HierarchyPOJO(String Year,
    		             String Quarter,
    		             String Month,
    		             String Date){
    	this.Year = Year;
    	this.Quarter = Quarter;
    	this.Month = Month;
    	this.Date = Date;
    }
}
